/******************************************************************************
 * include宣言
 */
#include <stdio.h>


/******************************************************************************
 * マクロ宣言
 */
#define SIZE_OF_ARRAY(arr)  ( sizeof(arr) / sizeof(arr[0]) )


/******************************************************************************
 * メイン
 */
int main(void)
{
    int arr[] = { 5, -4, 7, -8, 13, 1, -6 };

    for (int i = 0; i < SIZE_OF_ARRAY(arr); i++) {
        printf("arr[%d] = %d\n", i, arr[i]);
    }

    return 0;
}


/******************************************************************************
<実行結果>

arr[0] = 5
arr[1] = -4
arr[2] = 7
arr[3] = -8
arr[4] = 13
arr[5] = 1
arr[6] = -6

******************************************************************************/